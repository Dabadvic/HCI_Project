    private void btAnadeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAnadeActionPerformed
        // TODO add your handling code here:
        DefaultTableModel temp = (DefaultTableModel) tablaVenta.getModel();
        Object nuevo[] = {"", "", "", ""};
        int cod = Integer.parseInt(txCod.getText());
        String txt;
        Boolean found = false;
        Boolean repe = false;
        int i = 0;
        int aux;
        double aux_precio;
        //Se busca el producto a añadir en la lista de productos
        while ((!found) && (i < productos.length)) {
            if (productos[i].codigo == cod) {
                found = true;
            } else {
                i++;
            }
        }
        //Si no queda stock de un producto, no se puede añadir
        if (productos[i].stock <= 0) {
            found = false;
        }
        //Buscamos si el producto está ya en la tabla o no
        for (int j = 0; j < tablaVenta.getRowCount(); j++) {
            aux = Integer.parseInt(tablaVenta.getValueAt(j, 0).toString());
            if ((productos[i].codigo == aux) && (found)) {
                //Si está ya, incrementamos cantidad
                repe = true;
                //Aumentamos en uno la cantidad actual
                aux = Integer.parseInt(tablaVenta.getValueAt(j, 2).toString());
                tablaVenta.setValueAt(aux + 1, j, 2);
                //Aumentamos el precio
                aux_precio = Double.parseDouble(tablaVenta.getValueAt(j, 3).toString());
                tablaVenta.setValueAt(aux_precio + productos[i].precio, j, 3);
                //Se añade al precio final
                precio = precio + productos[i].precio;
            }
        }
        //Si es un nuevo producto:
        if ((found) && (!repe)) {
            //Añadir producto a la tabla
            temp.addRow(nuevo);
            tablaVenta.setValueAt(productos[i].codigo, tablaVenta.getRowCount() - 1, 0);
            tablaVenta.setValueAt(productos[i].nombre, tablaVenta.getRowCount() - 1, 1);
            tablaVenta.setValueAt(1, tablaVenta.getRowCount() - 1, 2);
            tablaVenta.setValueAt(productos[i].precio, tablaVenta.getRowCount() - 1, 3);
            //Añadir el precio del producto
            precio = precio + productos[i].precio;
        }
        //Se disminuye la cantidad restante en stock
        if (productos[i].stock > 0) {
            productos[i].stock--;
        }
        //Si no se encuentra, un mensaje indicará que el producto no está disponible
        if (!found) {
            JOptionPane.showMessageDialog(this,
                    "No hay disponibilidad del producto especificado.",
                    "Atención",
                    JOptionPane.WARNING_MESSAGE);
        }
        txt = "";
        txt += precio;
        panelVenta.setText("Precio final: " + txt);
        txCod.setText("");
    }//GEN-LAST:event_btAnadeActionPerformed

