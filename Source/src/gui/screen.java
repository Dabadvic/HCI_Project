    private void Ayuda() {
        String ruta = "Manual.pdf";
        File f = new File(ruta);
        if (f.exists()) {
            try {
                Runtime.getRuntime().exec("rundll32 url.dll,FileProtocolHandler " + ruta);
            } catch (Exception e) {
            }
        } else {
            JOptionPane.showMessageDialog(this, "No existe\n" + ruta, "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }

    /**
     * @brief Método usado para refrescar la lista de productos
     */
    private void RefrescarLista() {
        DefaultTableModel temp = (DefaultTableModel) TablaLista.getModel();
        Object nuevo[] = {"", "", ""};

        for (int i = 0; i < productos.length; i++) {
            if (i >= TablaLista.getRowCount()) {
                temp.addRow(nuevo);
            }
            TablaLista.setValueAt(productos[i].codigo, i, 0);
            TablaLista.setValueAt(productos[i].nombre, i, 1);
            TablaLista.setValueAt("" + productos[i].stock, i, 2);
        }
    }

